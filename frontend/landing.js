// Animate stat cards with counter effect
const statValues = document.querySelectorAll('.stat-value');
const animateCounter = (element, target, duration = 2000) => {
    const isPercentage = target.includes('%');
    const isTime = target.includes('s');
    const hasPlus = target.includes('+');
    const cleanTarget = parseFloat(target.replace(/[^\d.]/g, ''));

    let start = 0;
    const increment = cleanTarget / (duration / 16); // 60fps

    const counter = setInterval(() => {
        start += increment;
        if (start >= cleanTarget) {
            start = cleanTarget;
            clearInterval(counter);
        }

        let displayValue = Math.floor(start);
        if (target.includes('K')) {
            displayValue = (start / 1000).toFixed(1) + 'K';
        } else if (target.includes('M')) {
            displayValue = (start / 1000000).toFixed(1) + 'M';
        } else if (isPercentage) {
            displayValue = start.toFixed(1) + '%';
        } else if (isTime) {
            displayValue = '<' + Math.ceil(start) + 's';
        }

        if (hasPlus && start >= cleanTarget) {
            displayValue += '+';
        }

        element.innerHTML = displayValue;
    }, 16);
};

const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const target = entry.target.textContent;
            animateCounter(entry.target, target);
            statsObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

statValues.forEach(stat => {
    statsObserver.observe(stat);
});

// Button click effects
const buttons = document.querySelectorAll('.btn, .btn-nav');
buttons.forEach(button => {
    button.addEventListener('click', function (e) {
        // Create ripple effect
        const ripple = document.createElement('span');
        const rect = this.getBoundingClientRect();
        const size = Math.max(rect.width, rect.height);
        const x = e.clientX - rect.left - size / 2;
        const y = e.clientY - rect.top - size / 2;

        ripple.style.width = ripple.style.height = size + 'px';
        ripple.style.left = x + 'px';
        ripple.style.top = y + 'px';
        ripple.style.position = 'absolute';
        ripple.style.borderRadius = '50%';
        ripple.style.background = 'rgba(255, 255, 255, 0.5)';
        ripple.style.transform = 'scale(0)';
        ripple.style.animation = 'ripple 0.6s ease-out';
        ripple.style.pointerEvents = 'none';

        this.style.position = 'relative';
        this.style.overflow = 'hidden';
        this.appendChild(ripple);

        setTimeout(() => ripple.remove(), 600);
    });
});

